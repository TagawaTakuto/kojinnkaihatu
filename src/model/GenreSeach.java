package model;

public class GenreSeach {
	private int genreId;
	private int hikakuId;
	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getHikakuId() {
		return hikakuId;
	}

	public void setHikakuId(int hikakuId) {
		this.hikakuId = hikakuId;
	}

	public int getGenreId() {
		return genreId;
	}

	public void setGenreId(int genreId) {
		this.genreId = genreId;
	}
}

